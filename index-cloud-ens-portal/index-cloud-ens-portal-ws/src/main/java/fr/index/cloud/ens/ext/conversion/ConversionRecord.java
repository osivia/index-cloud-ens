package fr.index.cloud.ens.ext.conversion;



/**
 * The Class ConversionRecord.
 */
public class ConversionRecord {
    
    /** The field. */
    String field;
    
    /** The etablissement. */
    String etablissement;
    
    /** The publish code. */
    String publishCode;
    
    /** The publish label. */
    String publishLabel;
    
    /** The result code. */
    String resultCode;
    
    /**
     * Getter for field.
     * @return the field
     */
    public String getField() {
        return field;
    }
    
    /**
     * Setter for field.
     * @param field the field to set
     */
    public void setField(String field) {
        this.field = field;
    }
    
    /**
     * Getter for etablissement.
     * @return the etablissement
     */
    public String getEtablissement() {
        return etablissement;
    }
    
    /**
     * Setter for etablissement.
     * @param etablissement the etablissement to set
     */
    public void setEtablissement(String etablissement) {
        this.etablissement = etablissement;
    }
    
    /**
     * Getter for publishCode.
     * @return the publishCode
     */
    public String getPublishCode() {
        return publishCode;
    }
    
    /**
     * Setter for publishCode.
     * @param publishCode the publishCode to set
     */
    public void setPublishCode(String publishCode) {
        this.publishCode = publishCode;
    }
    
    /**
     * Getter for publishLabel.
     * @return the publishLabel
     */
    public String getPublishLabel() {
        return publishLabel;
    }
    
    /**
     * Setter for publishLabel.
     * @param publishLabel the publishLabel to set
     */
    public void setPublishLabel(String publishLabel) {
        this.publishLabel = publishLabel;
    }
    
    /**
     * Getter for resultCode.
     * @return the resultCode
     */
    public String getResultCode() {
        return resultCode;
    }
    
    /**
     * Setter for resultCode.
     * @param resultCode the resultCode to set
     */
    public void setResultCode(String resultCode) {
        this.resultCode = resultCode;
    }

}
